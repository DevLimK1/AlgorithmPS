package TIL.grammer.string;

public class String_indexOf {
    public static void main(String[] args) {
        String indexOfStringTest="Hello world";
        System.out.println("indexOf(\"o\") : "+indexOfStringTest.indexOf("o"));
        System.out.println("indexOf(\"o\",5): " +indexOfStringTest.indexOf("o",5));
    }
}
